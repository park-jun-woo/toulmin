//ff:func feature=tangl type=codegen control=sequence
//ff:what TestWriteHeader — tests writeHeader emits the generated-code banner and package line
package gen

import (
	"strings"
	"testing"
)

func TestWriteHeader(t *testing.T) {
	var w strings.Builder
	writeHeader(&w, "order flow")
	got := w.String()
	if !strings.Contains(got, "// Code generated by tangl. DO NOT EDIT.") {
		t.Errorf("expected banner comment, got %q", got)
	}
	if !strings.Contains(got, "package "+packageName("order flow")) {
		t.Errorf("expected package line, got %q", got)
	}
}
