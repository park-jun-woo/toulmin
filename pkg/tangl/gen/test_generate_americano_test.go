//ff:what TestGenerateAmericano — generates the spec's robot control example and checks key Go patterns
package gen

import (
	"go/format"
	"strings"
	"testing"

	"github.com/park-jun-woo/toulmin/pkg/tangl/parser"
)

// TestGenerateAmericano compiles the spec's "아메리카노 제조" example and
// checks it re-formats cleanly and contains the ticker/until Internal
// pattern plus a once-guarded do edge.
func TestGenerateAmericano(t *testing.T) {
	doc, err := parser.Parse("../parser/testdata/americano.md")
	if err != nil {
		t.Fatalf("Parse: %v", err)
	}
	src, err := Generate(doc)
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}
	if _, err := format.Source([]byte(src)); err != nil {
		t.Fatalf("re-format.Source: %v", err)
	}
	if !strings.HasPrefix(src, "// Code generated by tangl. DO NOT EDIT.") {
		t.Errorf("missing generated-file banner")
	}
	if !strings.Contains(src, "package americano") {
		t.Errorf("missing package clause; got:\n%s", src)
	}
	if !strings.Contains(src, "time.NewTicker(") {
		t.Errorf("missing ticker for the every-1s Internal trigger")
	}
	if !strings.Contains(src, "canServeCoffeeGraph.Evaluate(ctx)") {
		t.Errorf("missing until-clause Evaluate check on the ticker")
	}
	if !strings.Contains(src, `tangl.OnceDone(t.Ctx(), "once:americano.can place cup.order received#0")`) {
		t.Errorf("missing once-guard key for the placeCup do edge")
	}
	if !strings.Contains(src, "func MakeCoffee(ctx toulmin.Context) error {") {
		t.Errorf("missing the make coffee endpoint function")
	}
}
