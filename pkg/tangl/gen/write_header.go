//ff:func feature=tangl type=codegen control=sequence
//ff:what writeHeader — writes the generated-file banner and package clause
package gen

import (
	"fmt"
	"strings"
)

// writeHeader writes the generated-file banner and package clause.
func writeHeader(w *strings.Builder, subject string) {
	fmt.Fprintln(w, "// Code generated by tangl. DO NOT EDIT.")
	fmt.Fprintln(w)
	fmt.Fprintf(w, "package %s\n", packageName(subject))
	fmt.Fprintln(w)
}
